sumaTerminoNSucAritmetica= function(valor_inical,valor_final,n_vez)
{
  S_n = (n_vez /2)*(valor_inical+valor_final)
  
 print(paste0("La suma de los terminos de la sucesion que inicia en ",
              valor_inical, " y termina en ",valor_final," que es el termino "
              ,n_vez," de la susesion es de: ")) 
 return (S_n)
}
